#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;

	a = dest;
	b = (unsigned char *)src;
	while (n--)
	{
		if (a < b)
			*a++ = *b++;
		else
			((unsigned char *)dest)[n] = ((unsigned char *)src)[n];
	}
	return (dest);
}
