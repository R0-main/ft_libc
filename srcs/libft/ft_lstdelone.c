#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst)
		return ;
	if (*del)
	{
		del(lst->content);
	}
	else if (lst->content)
		free(lst->content);
	free(lst);
}
