#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i++ < n)
	{
		*((char *)dest) = *((const char *)src);
		dest++;
		src++;
	}
	return (dest - n);
}
